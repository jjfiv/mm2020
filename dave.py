def print_in_block(message):
  """
  print_in_block prints a message fancy
  """
  print("="*4, message)

print_in_block("knock knock!")
print_in_block("> who's there?")
print_in_block("orange")
print_in_block("> orange who?")
print_in_block("orange you glad...")


